# ─── Tests: Import Orders ─────────────────────────────────────────────────

@patch("serving.services.excel_importer.get_repository")
def test_import_orders_success(mock_get_repo, orders_excel_path, mock_repo):
    """Valid order rows should create orders and sales records."""
    # Mock product lookup to return existing products
    def get_record_with_products(table_name, filters=None, columns=None):
        if table_name == "products":
            sku = filters.get("sku_code") if filters else None
            if sku == "SKU-TEST-001":
                return [{"id": 1}]
            if sku == "SKU-TEST-002":
                return [{"id": 2}]
            if sku == "SKU-TEST-003":
                return [{"id": 3}]
            return [{"id": 1}]
        if table_name == "categories" and filters and filters.get("name"):
            return []
        if table_name == "customers":
            return []
        if table_name == "orders":
            return []
        return []

    mock_repo.get_record.side_effect = get_record_with_products
    mock_repo.add_record.return_value = True
    mock_get_repo.return_value = mock_repo

    result = import_orders_from_excel(orders_excel_path)

    assert result["status"] == "completed"
    assert result["total_rows"] == 3
    # 3 line items: ORD-001 has 2 items, ORD-002 has 1
    assert result["rows_imported"] == 3
    assert result["rows_failed"] == 0


