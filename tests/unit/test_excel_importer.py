# ─── Tests: Category Lookup ───────────────────────────────────────────────

def test_find_or_create_category_exists(mock_repo):
    """Category already exists — should return its ID."""
    # Override the side_effect from fixture with a fixed return
    mock_repo.get_record.side_effect = None
    mock_repo.get_record.return_value = [{"id": 5}]
    cat_id = _find_or_create_category("Electronics", mock_repo)
    assert cat_id == 5
    # Should not have tried to insert
    mock_repo.add_record.assert_not_called()


def test_find_or_create_category_creates_new(mock_repo):
    """Category doesn't exist — should create and return new ID."""
    # First call returns empty, second returns the newly created record
    mock_repo.get_record.side_effect = [[], [{"id": 10}]]
    cat_id = _find_or_create_category("New Category", mock_repo)
    assert cat_id == 10
    mock_repo.add_record.assert_called_once_with("categories", {"name": "New Category"})


